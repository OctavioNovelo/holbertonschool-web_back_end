#!/usr/bin/env python3
"""
Este script contiene una función para convertir un número flotante en su representación de cadena.
"""

def to_str(n: float) -> str:
    """
    Convierte un número de punto flotante a una cadena de texto.

    Args:
        n (float): El número de punto flotante a convertir.

    Returns:
        str: La representación en cadena del número flotante.
    """
    return str(n)
